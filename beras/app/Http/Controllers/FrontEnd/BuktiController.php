    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $cruds = new Bukti_Model();

        $cruds->trans_id = $request->id;

        $destination = base_path() . '/public/uploads/bukti';
        $request->file('foto')->move($destination, $request->id.".png");

        $cruds->bukti_foto = $request->id.".png";
        
        $cruds->save();

        return redirect()->route('cek_status.index');
    }

